/* =========================================================================
   VIS · Infographic mode
   A tall, poster-style single-column layout built from the analysis —
   ideal for sharing as one image. Exports to PNG.
   ========================================================================= */
(function () {
  window.VIS = window.VIS || {};

  function el(tag, cls, html) { var e = document.createElement(tag); if (cls) e.className = cls; if (html != null) e.innerHTML = html; return e; }
  function esc(s) { return String(s).replace(/[&<>]/g, function (c) { return { '&': '&amp;', '<': '&lt;', '>': '&gt;' }[c]; }); }

  function build(analysis, mount) {
    mount.innerHTML = '';
    var title = (document.getElementById('dashTitle') && document.getElementById('dashTitle').value) || analysis.title || 'Executive Report';

    var poster = el('div', 'info-poster'); poster.id = 'infoPoster';

    // Header
    poster.appendChild(el('div', 'info-header',
      '<div class="info-kicker">' + (analysis.aiEnhanced ? 'AI-POLISHED REPORT' : 'VISUAL INTELLIGENCE STUDIO') + '</div>' +
      '<h1 class="info-title">' + esc(analysis.headline || title) + '</h1>' +
      (analysis.tagline ? '<div class="info-tagline">' + esc(analysis.tagline) + '</div>' : '') +
      '<div class="info-date">' + new Date(analysis.meta.generatedAt).toLocaleDateString(undefined, { year: 'numeric', month: 'long', day: 'numeric' }) +
      ' · ' + analysis.meta.rows + ' records analyzed</div>'));

    // Hero stat
    var hero = analysis.kpis[0];
    if (hero) {
      var d = hero.delta;
      poster.appendChild(el('div', 'info-hero',
        '<div class="info-hero-label">' + esc(hero.label) + '</div>' +
        '<div class="info-hero-value">' + hero.formatted + '</div>' +
        (d != null ? '<div class="info-hero-delta ' + (d >= 0 ? 'up' : 'down') + '">' + (d >= 0 ? '\u25B2' : '\u25BC') + ' ' + VIS.engine.fmtSigned(d) + ' vs previous</div>' : '')));
    }

    // Stat strip
    var rest = analysis.kpis.slice(1, 5);
    if (rest.length) {
      poster.appendChild(el('div', 'info-stats', rest.map(function (k) {
        return '<div class="info-stat"><div class="info-stat-v">' + k.formatted + '</div><div class="info-stat-l">' + esc(k.label) + '</div></div>';
      }).join('')));
    }

    // Primary chart
    var mainChart = analysis.charts.find(function (c) { return c.role === 'trend' || c.role === 'comparison'; }) || analysis.charts[0];
    if (mainChart) {
      var block = el('div', 'info-chart-block', '<div class="info-block-title">' + esc(mainChart.title) + '</div><div class="info-chart"></div>');
      poster.appendChild(block);
      block._spec = mainChart;
    }

    // Summary
    poster.appendChild(el('div', 'info-summary',
      '<div class="info-block-title">The Story</div><p>' + analysis.summary + '</p>'));

    // Secondary chart (composition)
    var second = analysis.charts.find(function (c) { return c.role === 'composition' || c.role === 'hierarchy'; });
    if (second && second !== mainChart) {
      var block2 = el('div', 'info-chart-block', '<div class="info-block-title">' + esc(second.title) + '</div><div class="info-chart info-chart-sm"></div>');
      poster.appendChild(block2);
      block2._spec = second;
    }

    // Findings
    if (analysis.insights && analysis.insights.length) {
      poster.appendChild(el('div', 'info-findings',
        '<div class="info-block-title">Key Findings</div>' +
        analysis.insights.slice(0, 4).map(function (i) {
          return '<div class="info-find ' + i.type + '"><span class="info-find-ic" data-ic="' + i.icon + '"></span><span>' + i.text + '</span></div>';
        }).join('')));
    }

    // Recommendations
    if (analysis.recommendations && analysis.recommendations.length) {
      poster.appendChild(el('div', 'info-recs',
        '<div class="info-block-title">Recommendations</div>' +
        '<ol>' + analysis.recommendations.map(function (r) { return '<li>' + esc(r) + '</li>'; }).join('') + '</ol>'));
    }

    // Footer
    poster.appendChild(el('div', 'info-footer', 'Generated by VIS · Visual Intelligence Studio · Created by Keovoin'));

    mount.appendChild(poster);
    VIS.hydrateIcons(poster);

    // render charts after layout
    requestAnimationFrame(function () {
      requestAnimationFrame(function () {
        poster.querySelectorAll('.info-chart-block').forEach(function (b) {
          if (b._spec) { var node = b.querySelector('.info-chart'); if (node) VIS.charts.build(b._spec, node); }
        });
      });
    });
  }

  window.VIS.infographic = { build: build };
})();
