/* =========================================================================
   VIS · Presentation generator
   Converts the current analysis into a navigable slide deck:
   Title -> Key metrics -> one slide per chart -> Summary -> Findings ->
   Recommendations. Supports keyboard nav, fullscreen, and PDF export.
   ========================================================================= */
(function () {
  window.VIS = window.VIS || {};
  var deck = { slides: [], index: 0, analysis: null };

  function el(tag, cls, html) { var e = document.createElement(tag); if (cls) e.className = cls; if (html != null) e.innerHTML = html; return e; }
  function fmt(n, sub) { return VIS.engine.fmtNumber(n, sub); }

  function build(analysis, mount) {
    deck.analysis = analysis; deck.index = 0; deck.slides = [];
    VIS.charts && VIS.charts.disposeAll && null; // charts here are tracked separately by echarts
    mount.innerHTML = '';

    var stage = el('div', 'slide-stage'); stage.id = 'slideStage';
    var slidesWrap = el('div', 'slides');

    var title = (document.getElementById('dashTitle') && document.getElementById('dashTitle').value) || analysis.title || 'Executive Dashboard';

    // Slide 1 — Title
    slidesWrap.appendChild(slide('title',
      '<div class="s-title-block">' +
        '<div class="s-kicker">' + (analysis.aiEnhanced ? 'AI-POLISHED BRIEFING' : 'VISUAL INTELLIGENCE STUDIO') + '</div>' +
        '<h1 class="s-title">' + esc(title) + '</h1>' +
        (analysis.tagline ? '<div class="s-sub" style="font-size:clamp(15px,2vw,24px);color:var(--text);font-weight:500">' + esc(analysis.tagline) + '</div>' : '') +
        '<div class="s-sub">' + analysis.meta.rows + ' records · ' + analysis.meta.measures + ' key measures · ' + new Date(analysis.meta.generatedAt).toLocaleDateString() + '</div>' +
      '</div>'));

    // Slide 2 — Key metrics
    var kpiCards = analysis.kpis.slice(0, 6).map(function (k) {
      var d = k.delta;
      var deltaHtml = d != null ? '<div class="s-kpi-delta ' + (d >= 0 ? 'up' : 'down') + '">' + (d >= 0 ? '\u25B2' : '\u25BC') + ' ' + VIS.engine.fmtSigned(d) + '</div>' : '';
      return '<div class="s-kpi"><div class="s-kpi-label">' + esc(k.label) + '</div><div class="s-kpi-value">' + k.formatted + '</div>' + deltaHtml + '</div>';
    }).join('');
    slidesWrap.appendChild(slide('metrics', '<h2 class="s-h2">Key Metrics</h2><div class="s-kpi-grid">' + kpiCards + '</div>'));

    // Chart slides
    analysis.charts.forEach(function (spec) {
      var s = slide('chart', '<h2 class="s-h2">' + esc(spec.title) + '</h2><div class="s-chart" data-chartspec="1"></div>');
      s._chartSpec = spec;
      slidesWrap.appendChild(s);
    });

    // Summary slide
    slidesWrap.appendChild(slide('summary',
      '<div class="s-summary"><div class="s-kicker">EXECUTIVE SUMMARY</div><p>' + analysis.summary + '</p></div>'));

    // Findings slide
    if (analysis.insights && analysis.insights.length) {
      var f = analysis.insights.slice(0, 5).map(function (i) {
        return '<li class="s-find ' + i.type + '"><span class="s-find-ic" data-ic="' + i.icon + '"></span><span>' + i.text + '</span></li>';
      }).join('');
      slidesWrap.appendChild(slide('findings', '<h2 class="s-h2">Key Findings</h2><ul class="s-find-list">' + f + '</ul>'));
    }

    // Recommendations slide
    if (analysis.recommendations && analysis.recommendations.length) {
      var r = analysis.recommendations.map(function (x) { return '<li>' + esc(x) + '</li>'; }).join('');
      slidesWrap.appendChild(slide('recs', '<h2 class="s-h2">Recommendations</h2><ol class="s-rec-list">' + r + '</ol>'));
    }

    // Closing slide
    slidesWrap.appendChild(slide('title',
      '<div class="s-title-block"><div class="s-kicker">THANK YOU</div><h1 class="s-title">Questions &amp; Discussion</h1>' +
      '<div class="s-sub">Generated by VIS · Visual Intelligence Studio</div></div>'));

    stage.appendChild(slidesWrap);

    // Controls
    var controls = el('div', 'slide-controls',
      '<button class="icon-btn" id="slidePrev"><span data-ic="arrowLeft"></span></button>' +
      '<span class="slide-count" id="slideCount">1 / 1</span>' +
      '<button class="icon-btn" id="slideNext"><span data-ic="arrowRight"></span></button>' +
      '<button class="icon-btn" id="slideFull" title="Fullscreen"><span data-ic="expand"></span></button>');

    mount.appendChild(stage);
    mount.appendChild(controls);

    deck.slides = Array.prototype.slice.call(slidesWrap.children);
    VIS.hydrateIcons(mount);

    document.getElementById('slidePrev').addEventListener('click', prev);
    document.getElementById('slideNext').addEventListener('click', next);
    document.getElementById('slideFull').addEventListener('click', function () { toggleFull(stage); });

    show(0);
    return deck.slides.length;
  }

  function slide(kind, html) { var s = el('div', 'slide slide-' + kind, html); return s; }
  function esc(s) { return String(s).replace(/[&<>]/g, function (c) { return { '&': '&amp;', '<': '&lt;', '>': '&gt;' }[c]; }); }

  function show(i) {
    if (!deck.slides.length) return;
    deck.index = Math.max(0, Math.min(deck.slides.length - 1, i));
    deck.slides.forEach(function (s, idx) { s.classList.toggle('active', idx === deck.index); });
    var counter = document.getElementById('slideCount');
    if (counter) counter.textContent = (deck.index + 1) + ' / ' + deck.slides.length;
    // lazy render chart for this slide
    var cur = deck.slides[deck.index];
    if (cur && cur._chartSpec && !cur._chartRendered) {
      var node = cur.querySelector('.s-chart');
      if (node) {
        cur._chartRendered = true;
        requestAnimationFrame(function () { requestAnimationFrame(function () { VIS.charts.build(cur._chartSpec, node); }); });
      }
    } else {
      setTimeout(function () { VIS.charts.resizeAll(); }, 80);
    }
  }
  function next() { show(deck.index + 1); }
  function prev() { show(deck.index - 1); }

  function toggleFull(stage) {
    if (!document.fullscreenElement) { (stage.requestFullscreen || stage.webkitRequestFullscreen || function () {}).call(stage); }
    else { (document.exitFullscreen || document.webkitExitFullscreen || function () {}).call(document); }
    setTimeout(function () { VIS.charts.resizeAll(); }, 300);
  }

  // keyboard nav (only when presentation view is active)
  document.addEventListener('keydown', function (e) {
    var pv = document.querySelector('[data-view="presentation"]');
    if (!pv || !pv.classList.contains('active')) return;
    if (e.key === 'ArrowRight' || e.key === 'PageDown') { e.preventDefault(); next(); }
    else if (e.key === 'ArrowLeft' || e.key === 'PageUp') { e.preventDefault(); prev(); }
  });

  function exportPDF() {
    if (!deck.slides.length) { return; }
    if (typeof htmlToImage === 'undefined' || !window.jspdf) { VIS.toast && VIS.toast('Export libraries not loaded'); return; }
    var jsPDF = window.jspdf.jsPDF;
    var pdf = new jsPDF({ orientation: 'l', unit: 'px', format: [1280, 720] });
    var bg = getComputedStyle(document.documentElement).getPropertyValue('--surface').trim() || '#fff';
    // ensure all chart slides render first
    deck.slides.forEach(function (s, idx) {
      if (s._chartSpec && !s._chartRendered) { s._chartRendered = true; var n = s.querySelector('.s-chart'); if (n) VIS.charts.build(s._chartSpec, n); }
    });
    var i = 0;
    function step() {
      if (i >= deck.slides.length) { pdf.save(pptFilename()); return; }
      var s = deck.slides[i];
      var wasActive = s.classList.contains('active');
      s.classList.add('active'); s.style.opacity = '1';
      htmlToImage.toPng(s, { pixelRatio: 1.5, backgroundColor: bg, width: s.offsetWidth, height: s.offsetHeight }).then(function (url) {
        if (i > 0) pdf.addPage([1280, 720], 'l');
        pdf.addImage(url, 'PNG', 0, 0, 1280, 720);
        if (!wasActive) s.classList.remove('active');
        i++; setTimeout(step, 60);
      }).catch(function (err) { console.error(err); i++; step(); });
    }
    step();
  }

  function pptFilename() {
    var t = ((document.getElementById('dashTitle') && document.getElementById('dashTitle').value) || 'vis-presentation').toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
    return t + '-slides-' + new Date().toISOString().slice(0, 10) + '.pdf';
  }

  window.VIS.present = { build: build, next: next, prev: prev, show: show, exportPDF: exportPDF, get deck() { return deck; } };
})();
